import fs from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const rootDir = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const srcDir = path.join(rootDir, "src");
const outputPath = path.join(rootDir, "RoSync.plugin.luau");

const moduleEntries = [
  ["main.client", "main.client.luau"],
  ["network/HttpClient", "network/HttpClient.luau"],
  ["network/WebSocketClient", "network/WebSocketClient.luau"],
  ["sync/Listener", "sync/Listener.luau"],
  ["sync/Serializer", "sync/Serializer.luau"],
  ["sync/Deserializer", "sync/Deserializer.luau"],
  ["util/PathUtil", "util/PathUtil.luau"],
  ["util/SchemaUtil", "util/SchemaUtil.luau"],
];

function luaQuote(text) {
  return `'${text.replace(/\\/g, "\\\\").replace(/'/g, "\\'")}'`;
}

function scriptPathForId(moduleId) {
  if (moduleId === "main.client") {
    return "script";
  }

  const parts = moduleId.split("/");
  const variablePath = ["__root"];
  for (const part of parts) {
    variablePath.push(part);
  }
  return variablePath.join(".");
}

function buildTreeShape(entries) {
  const tree = {};
  for (const [moduleId] of entries) {
    const parts = moduleId.split("/");
    if (parts[0] === "main.client") {
      tree["main.client"] = {};
      continue;
    }

    let current = tree;
    for (const part of parts) {
      current[part] = current[part] ?? {};
      current = current[part];
    }
  }
  return tree;
}

function emitTree(tree, parentVar, lines) {
  const keys = Object.keys(tree).sort();
  for (const key of keys) {
    if (key === "main.client") {
      continue;
    }

    const currentVar = `${parentVar}.${key}`;
    lines.push(`${currentVar} = { Name = ${luaQuote(key)}, Parent = ${parentVar} }`);
    emitTree(tree[key], currentVar, lines);
  }
}

async function main() {
  const moduleBodies = new Map();
  for (const [moduleId, relativePath] of moduleEntries) {
    const filePath = path.join(srcDir, relativePath);
    const body = await fs.readFile(filePath, "utf8");
    moduleBodies.set(moduleId, body.replace(/\r\n/g, "\n"));
  }

  const lines = [];
  lines.push("-- Auto-generated by plugin/tools/bundle.mjs");
  lines.push("-- Do not edit this file directly; edit plugin/src/* and rebuild.");
  lines.push("");
  lines.push("local __modules = {}");
  lines.push("local __cache = {}");
  lines.push("local __root = { Name = 'src', Parent = nil }");
  lines.push("local script = { Name = 'main.client', Parent = __root, __moduleId = 'main.client' }");

  const tree = buildTreeShape(moduleEntries);
  emitTree(tree, "__root", lines);

  for (const [moduleId] of moduleEntries) {
    if (moduleId === "main.client") {
      continue;
    }
    const scriptRef = scriptPathForId(moduleId);
    lines.push(`${scriptRef}.__moduleId = ${luaQuote(moduleId)}`);
  }

  lines.push("");
  lines.push("local function __require(target)");
  lines.push("\tlocal moduleId = type(target) == 'table' and target.__moduleId or target");
  lines.push("\tif __cache[moduleId] ~= nil then");
  lines.push("\t\treturn __cache[moduleId]");
  lines.push("\tend");
  lines.push("\tlocal loader = __modules[moduleId]");
  lines.push("\tif not loader then");
  lines.push("\t\terror('Unknown bundled module: ' .. tostring(moduleId))");
  lines.push("\tend");
  lines.push("\tlocal moduleScript = moduleId == 'main.client' and script or " +
    moduleEntries
      .filter(([moduleId]) => moduleId !== "main.client")
      .map(([moduleId]) => `(moduleId == ${luaQuote(moduleId)} and ${scriptPathForId(moduleId)})`)
      .join(" or ") +
    " or script");
  lines.push("\tlocal result = loader(__require, moduleScript)");
  lines.push("\tif result == nil then");
  lines.push("\t\tresult = true");
  lines.push("\tend");
  lines.push("\t__cache[moduleId] = result");
  lines.push("\treturn result");
  lines.push("end");
  lines.push("");

  for (const [moduleId] of moduleEntries) {
    lines.push(`__modules[${luaQuote(moduleId)}] = function(moduleRequire, script)`);
    lines.push("\tlocal require = moduleRequire");
    lines.push(moduleBodies.get(moduleId).split("\n").map((line) => `\t${line}`).join("\n"));
    lines.push("end");
    lines.push("");
  }

  lines.push("__modules['main.client'](__require, script)");
  lines.push("");

  await fs.writeFile(outputPath, lines.join("\n"), "utf8");
  console.log(`Bundled ${path.relative(process.cwd(), outputPath)}`);
}

await main();
